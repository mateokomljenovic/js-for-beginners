// use let
let something; 

//number to string

something = String(5)

//boolean to string

something = String(true)

//Date to string

something = String(new Date())

//Array to string

something = String([1 ,2, 3, 4])

//toString()

something = (5).toString()

// Boolean with toString()

something = (true).toString()

// String to number

something = Number('5')

//Boolean to number

something = Number('hello')

// parseInt

something = parseInt('100.30')



//output
console.log(something)
